COMMENT ON COLUMN DENSO.DNKR_COMMON_CODE.CMC_REGDATE IS '변경일자';

COMMENT ON COLUMN DENSO.DNKR_COMMON_CODE.CMC_USE_YN IS '코드사용여부(Y,N)';

COMMENT ON COLUMN DENSO.DNKR_COMMON_CODE.CMC_TYPE IS '코드유형(MAIN,SUB)';


