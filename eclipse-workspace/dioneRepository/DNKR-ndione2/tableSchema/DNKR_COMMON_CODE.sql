  CMC_REGDATE      DATE,
  CMC_USE_YN       CHAR(1 BYTE)                 DEFAULT 'Y'                   NOT NULL,
  CMC_TYPE         VARCHAR2(4 BYTE)             DEFAULT 'MAIN'                NOT NULL
)
TABLESPACE DRM
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
MONITORING;

