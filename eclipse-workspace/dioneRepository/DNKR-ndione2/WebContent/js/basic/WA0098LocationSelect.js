/*
====================================================================
[EN] Function: Return selected location information to Parent Screen.
[EN] Argument: [1st argument] location number
[EN]           [2nd argument] location name
[EN] Summary : I return selected location information to Parent Screen.
====================================================================
*/
function returnValue(lnum, lname) {
    window.opener.document.getElementById("locationNo").value = lnum;
    window.opener.document.getElementById("locationName").value = lname;
    window.close();
}

