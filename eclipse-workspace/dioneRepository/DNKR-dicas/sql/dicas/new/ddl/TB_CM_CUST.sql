CREATE TABLE TB_CM_CUST (
  CUST_ID       NUMBER(20) NOT NULL,
  RGSTR_ID      NUMBER(20) NOT NULL,
  RGST_DT       TIMESTAMP NOT NULL,
  UPDTR_ID      NUMBER(20) NOT NULL,
  UPDT_DT       TIMESTAMP NOT NULL,
  CUST_NM       VARCHAR(255) NOT NULL,
  CUST_FULNM    VARCHAR(255) NOT NULL,
  CUST_SE       VARCHAR(20) NOT NULL,
  ITM_GRP_SE    VARCHAR(20) NOT NULL,
  CUST_CD       VARCHAR(20) NOT NULL,
  TEL_NO        VARCHAR(20),
  FAX_NO        VARCHAR(20),
  ZIP           VARCHAR(10),
  ADRES         VARCHAR(255),
  ROAD_ADRES    VARCHAR(255),
  DTL_ADRES     VARCHAR(255),
  CONSTRAINT PK_CM_CUST
  UNIQUE (CUST_ID)
