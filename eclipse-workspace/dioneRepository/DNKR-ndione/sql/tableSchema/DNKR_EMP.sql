CREATE TABLE DENSO.DNKR_EMP
(
  EMP_NUMBER        VARCHAR2(10 BYTE)           NOT NULL,
  CLS_CODE          VARCHAR2(45 BYTE),
  LOC_CODE          VARCHAR2(45 BYTE),
  TEAM_CODE         VARCHAR2(45 BYTE),
  AUTH_CODE         VARCHAR2(45 BYTE),
  EMP_NAME          VARCHAR2(40 BYTE),
  EMP_EMAIL         VARCHAR2(100 BYTE),
  EMP_ID            VARCHAR2(20 BYTE),
  EMP_SYNC_DATE     DATE,
  EMP_USE           CHAR(1 BYTE)                DEFAULT 'Y'                   NOT NULL,
  EMP_PWD           VARCHAR2(45 BYTE),
  EMP_PRIVATE_AUTH  CHAR(1 BYTE)                DEFAULT 'N'                   NOT NULL
)
TABLESPACE DRM
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
MONITORING;

COMMENT ON COLUMN DENSO.DNKR_EMP.EMP_NUMBER IS '사번';

