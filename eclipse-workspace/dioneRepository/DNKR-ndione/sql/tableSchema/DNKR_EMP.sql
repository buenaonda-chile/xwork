COMMENT ON COLUMN DENSO.DNKR_EMP.EMP_NAME IS '사원명';

COMMENT ON COLUMN DENSO.DNKR_EMP.EMP_EMAIL IS '이메일';

COMMENT ON COLUMN DENSO.DNKR_EMP.EMP_ID IS '아이디';

COMMENT ON COLUMN DENSO.DNKR_EMP.EMP_SYNC_DATE IS '원본데이터 동기화 일자';

COMMENT ON COLUMN DENSO.DNKR_EMP.EMP_USE IS '사용여부(Y,N)';

COMMENT ON COLUMN DENSO.DNKR_EMP.EMP_PWD IS '패스워드';

